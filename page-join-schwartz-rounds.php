<h4>Benefits of the Schwartz Rounds Program</h4>
<p>Schwartz Rounds sessions strengthen the caregiver-patient relationship and remind caregivers why they entered the healthcare profession. A comprehensive evaluation has shown that the program has a unique and profound impact on caregivers as well as host organizations.</p>
